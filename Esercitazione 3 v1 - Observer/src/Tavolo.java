
public class Tavolo {
	
	private int posti;
	private int numeroTavolo;
	
	public Tavolo(int posti, int numeroTavolo){
		this.posti=posti;
		this.numeroTavolo=numeroTavolo;
	}
	
	public int getPosti(){
		return this.posti;
	}
	
	public int getTavolo(){
		return this.numeroTavolo;
	}
	
}
